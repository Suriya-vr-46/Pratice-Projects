package Zoo;

public class Animal {
	protected String name;
	protected int age;
	protected int weight;
	protected String sound;
	protected String sepcialBehaviour;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}

	public String getSound() {
		return sound;
	}

	public void setSound(String sound) {
		this.sound = sound;
	}

	public String getSepcialBehaviour() {
		return sepcialBehaviour;
	}

	public void setSepcialBehaviour(String sepcialBehaviour) {
		this.sepcialBehaviour = sepcialBehaviour;
	}

}
